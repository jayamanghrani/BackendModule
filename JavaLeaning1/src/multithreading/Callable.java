package multithreading;

public interface Callable {

/*	The Callable interface is similar to Runnable, in that both are designed for classes 
	whose instances are potentially executed by another thread.
	A Runnable, however, does not return a result and cannot throw a checked exception.*/
	
	
/*	
	1) The Runnable interface is older than Callable which is there from JDK 1.0, 
	while Callable is added on Java 5.0.

	2) Runnable interface has run() method to define task while Callable interface uses call() method 
	for task definition.

	3) run() method does not return any value, its return type is void while the call method returns a value. 
		The Callable interface is a generic parameterized interface and a Type of value is provided 
		when an instance of Callable implementation is created.

	4) Another difference in the run and call method is that the run method can not throw checked exceptions 
	while the call method can throw checked exceptions in Java.
	
	*/
	
	
	
	
	
	
	
	
	
	
	
}
