//The three important keywords for Exception Handling are try, catch, and finally. 

/** Try what?  */
/*
 * First you try to execute your code. If it doesn’t execute as planned, you
 * handle the exceptional conditions using a catch block.
 */


/** Catch what?  */
/*
 * You catch the exceptional event arising from the code enclosed within
 * the try block and handle the event by defining appropriate exception
 * handlers.
 */

/** What does finally do?  */
/*
 * Finally, you execute a set of code, in all conditions, regardless of whether
 * the code in the try block throws any exceptions.
 */